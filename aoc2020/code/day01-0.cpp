#define Assert(Condition) if(!(Condition)) { *(int *)0 = 0; }
#define ArrayCount(Array) (sizeof((Array)) / sizeof((Array)[0]))

int
main(void)
{
    int32_t *Low = Numbers;
    int32_t *High = Numbers + (ArrayCount(Numbers) - 1);
    int32_t Target = 2020;

    for(;;)
    {
        Assert(Low < High);

        int32_t Sum = *Low + *High;
        if(Sum > Target)
        {
            --High;
        }
        else if(Sum < Target)
        {
            ++Low;
        }
        else if(Sum == Target)
        {
            break;
        }
    }

    printf("%d found (%d + %d)\n", Target, *Low, *High);
    printf("Answer: %d\n", (*Low)*(*High));

    return(0);
}
